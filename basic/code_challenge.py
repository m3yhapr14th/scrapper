# Calculator
def plus(a, b):
    return a + b
def minus(a, b):
    return a - b
def gop(a, b):
    return a * b
def nanu(a, b):
    return a / b
def remain(a, b):
    return a % b
def power(a, b):
    return a ** b

a = 12
b = "10"
print(plus(a, int(b)))
print(minus(a, int(b)))
print(gop(a, int(b)))
print(nanu(a, int(b)))
print(remain(a, int(b)))
print(power(a, int(b)))
